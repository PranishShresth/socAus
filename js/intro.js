// Data types
// String, Number, Boolean, null, undefined , symbol, arrays, object
// Primitive type
// String, Number, Boolean, null, undefined , symbol,
// Compositive type
// arrays, object

// String
var string = "This is a string";
console.log(string);

// Number
var number = 1000;
var number1 = 1000.2323;
console.log(number, number1);

// Boolean
var sayYes = true;
var sayNo = false;
console.log(sayYes, sayNo);

// Undefined
var jeremy;

// Null
var lucian = null;

// Symbol
let sym = Symbol();

console.log(jeremy);

console.log(lucian);
console.log(sym);

// Compositive
var arrays = [
  "Lucian",
  "Jeremy",
  23,
  1002,
  null,
  undefined,
  Symbol(),
  ["Hello world"],
];
console.log(arrays);

// Objects
var object = {
  key: "value",
  lucian: "gold",
  jeremy: 1002,
};
